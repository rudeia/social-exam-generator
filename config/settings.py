# 전체 단원 통합 사전
# ──────────────────────────────────────────────
ALL_UNITS = {**UNITS_SOCIAL1, **UNITS_SOCIAL2}

def get_unit_display_name(unit_key: str) -> str:
    """예: 'Ⅰ' → 'Ⅰ. 통합적 관점'"""
    unit = ALL_UNITS.get(unit_key)
    if unit:
        return f"{unit_key}. {unit['name']}"
    return unit_key

def get_unit_list_for_display() -> list:
    """UI 선택용 리스트 반환"""
    result = []
    for key in ALL_UNITS:
        unit = ALL_UNITS[key]
        result.append({
            "key": key,
            "display": f"{key}. {unit['name']}",
            "subject": unit["subject"],
        })
    return result

def get_standards_text(unit_key: str) -> str:
    """특정 단원의 성취기준을 텍스트로 반환"""
    unit = ALL_UNITS.get(unit_key)
    if not unit:
        return ""
    lines = []
    for s in unit["standards"]:
        lines.append(f"[{s['code']}] {s['desc']}")
        lines.append(f"  - 핵심개념: {s['key_concepts']}")
        lines.append(f"  - 출제포인트: {s['exam_points']}")
        lines.append("")
    return "\n".join(lines)

def get_all_standards_text() -> str:
    """전체 성취기준 텍스트"""
    lines = []
    for key in ALL_UNITS:
        unit = ALL_UNITS[key]
        lines.append(f"\n== {key}. {unit['name']} ({unit['subject']}) ==")
        for s in unit["standards"]:
            lines.append(f"[{s['code']}] {s['desc']}")
            lines.append(f"  핵심개념: {s['key_concepts']}")
            lines.append(f"  출제포인트: {s['exam_points']}")
    return "\n".join(lines)
